bool menuInput()
{
    int counter = 1;
    menu(counter);

    do
    {
        // Switch case for switching around the main menu
        switch (_getch())
        {
            // Arrow up
        case KEY_UP:
        {
            if (counter == 1) counter = 4;
            counter--;
            system("CLS");
            menu(counter);
        } break;
        // Arrow down
        case KEY_DOWN:
        {
            if (counter == 3) {
                counter = 0;
            }
            counter++;
            system("CLS");
            menu(counter);
        } break;
        // Enter
        case ENTER:
        {
            switch (counter) {
            case 1:
            {
                system("CLS");
                cout << "LESSON";
                break;
            }
           
            case 2:
            {
                system("CLS");
                cout << "HOW TO USE";
                menuInput();
                break;
            }

            case 3:
            {
                return 0;
            }
          
            }
        } break;
        }
    } while (true);
    return 1;
}

